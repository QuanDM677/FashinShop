    @Override
    public void selectTimeRange() {
        TimeRange range = TimeRange.today();
        switch (cboTimeRanges3.getSelectedIndex()) {
            case 0 ->
                range = TimeRange.today();
            case 1 ->
                range = TimeRange.thisWeek();
            case 2 ->
                range = TimeRange.thisMonth();
            case 3 ->
                range = TimeRange.thisQuarter();
            case 4 ->
                range = TimeRange.thisYear();
        }
        txtBegin.setText(XDate.format(range.getBegin(), "MM/dd/yyyy"));
        txtEnd.setText(XDate.format(range.getEnd(), "MM/dd/yyyy"));

        this.fillRevenue();
    }

    private boolean validateFilter() {
        if (txtBegin.getText().isBlank()) {
            XDialog.warning("Vui l√≤ng nh·∫≠p ng√†y b·∫Øt ƒë·∫ßu!");
            txtBegin.requestFocus();
            return false;
        }
        if (txtEnd.getText().isBlank()) {
            XDialog.warning("Vui l√≤ng nh·∫≠p ng√†y k·∫øt th√∫c!");
            txtEnd.requestFocus();
            return false;
        }
        Date begin = XDate.parse(txtBegin.getText(), "MM/dd/yyyy");
        Date end = XDate.parse(txtEnd.getText(), "MM/dd/yyyy");
        if (begin == null) {
            XDialog.warning("Ng√†y b·∫Øt ƒë·∫ßu kh√¥ng ƒë√∫ng ƒë·ªãnh d·∫°ng MM/dd/yyyy!");
            txtBegin.requestFocus();
            return false;
        }
        if (end == null) {
            XDialog.warning("Ng√†y k·∫øt th√∫c kh√¥ng ƒë√∫ng ƒë·ªãnh d·∫°ng MM/dd/yyyy!");
            txtEnd.requestFocus();
            return false;
        }
        if (begin.after(end)) {
            XDialog.warning("Ng√†y b·∫Øt ƒë·∫ßu ph·∫£i nh·ªè h∆°n ho·∫∑c b·∫±ng ng√†y k·∫øt th√∫c!");
            txtBegin.requestFocus();
            return false;
        }
        return true;
    }

    @Override
    public void fillRevenue() {
        // B·ªè validateFilter() ·ªü ƒë√¢y, ch·ªâ c·∫ßn x·ª≠ l√Ω n·∫øu r·ªóng th√¨ l·∫•y m·∫∑c ƒë·ªãnh
        Date begin = XDate.parse(txtBegin.getText(), "MM/dd/yyyy");
        Date end = XDate.parse(txtEnd.getText(), "MM/dd/yyyy");

        // N·∫øu ng√†y b·ªã r·ªóng ho·∫∑c null th√¨ l·∫•y ng√†y m·∫∑c ƒë·ªãnh (v√≠ d·ª• h√¥m nay)
        if (begin == null) {
            begin = new Date();
        }
        if (end == null) {
            end = new Date();
        }

        if (end != null) {
            java.util.Calendar cal = java.util.Calendar.getInstance();
            cal.setTime(end);
            cal.set(java.util.Calendar.HOUR_OF_DAY, 23);
            cal.set(java.util.Calendar.MINUTE, 59);
            cal.set(java.util.Calendar.SECOND, 59);
            cal.set(java.util.Calendar.MILLISECOND, 999);
            end = cal.getTime();
        }
        switch (tabs.getSelectedIndex()) {
            case 0 ->
                this.fillRevenueByCategory(begin, end);
            case 1 ->
                this.fillRevenueByUser(begin, end);
            case 2 ->
                this.fillRevenueBySummary(begin, end);
        }
    }

    private void fillRevenueByCategory(Date begin, Date end) {
        List<Revenue.ByCategory> items = dao.getByCategory(begin, end);
        DefaultTableModel model = (DefaultTableModel) tblByCategory.getModel();
        model.setRowCount(0);
        if (items == null || items.isEmpty()) {
            if (isFilterButton) {
                XDialog.info("Kh√¥ng c√≥ d·ªØ li·ªáu doanh thu theo lo·∫°i trong kho·∫£ng th·ªùi gian n√†y!");
            }
            return;
        }
        items.forEach(item -> {
            Object[] row = {
                item.getCategory(),
                String.format("$%.2f", item.getRevenue()),
                item.getQuantity(),
                String.format("$%.2f", item.getMinPrice()),
                String.format("$%.2f", item.getMaxPrice()),
                String.format("$%.2f", item.getAvgPrice())
            };
            model.addRow(row);
        });
    }

